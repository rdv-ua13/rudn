                            <p>Одно из основных направлений переработки биоматериалов — создание биотоплива. В большинстве случаев в качестве первичного сырья используют лигноцеллюлозу — сухие отходы сельского хозяйства, лесной и бумажной промышленности. Одно из составных веществ лигноцеллюлозы — оксиметилфурфурол (HMF). Из него получают эфиры, которые можно использовать как экологически чистое топливо. Химик РУДН создал молекулярный каркас, или ксерогель на основе веществ гетерополикислот Прейсслера, который в 4-10 раз повышает эффективность получения эфиров из HMF.</p>
                            <p>Эфиры из HMF получаются при взаимодействии со спиртами — исследователи использовали бутанол. Катализатором обычно выступают так называемые кислоты Прейслера. Они могут катализировать реакцию и сами по себе, однако химики РУДН нашли способ повысить их активность. Для этого исследователи создали ксерогель из диоксида кремния и нанесли на него кислоты Прейслера. Молекулы кислот распределились по молекулярному каркасу ксерогеля, и благодаря этому увеличилась площадь соприкосновения катализатора и HMF. В результате кремниевый ксерогель увеличил конверсию реакции — количество HMF, которое вступает в реакцию, — и селективность реакции, то есть количество эфира, которое получается на выходе.</p>
                            <p>Экспериментальным путем химики определили оптимальные параметры реакции — температура 100 ℃ и соотношение HMF к бутанолу 1:3. При таких условиях конверсия достигла 89%, а селективность — 73%. Для сравнения «чистые» кислоты Прейслера дают селективность от 8% до 23%. Таким образом, благодаря каталитическому комплексу для получения того же количества эфира нужно в несколько раз меньше исходного продукта. Это поможет сократить энергозатраты и удешевит производство. Более того, после применения ксерогель можно промыть этиловым спиртом и использовать снова — и так до 5 раз. При этом конверсия и селективность за 5 циклов использования снижаются всего лишь до 50% и 60% соответственно.</p>
